    public void listaTabela(){
        IBilheteDAO dao = new BilheteDAO();
        List<Bilhete> lista = dao.listarTodos();
        
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("idNota");
        modelo.addColumn("idFuncionario");
        modelo.addColumn("data");
        modelo.addColumn("CPF");
        modelo.addColumn("tipoPagamento");
        modelo.addColumn("qtdIngressos");
        
        for(Bilhete bilhete: lista){
            modelo.addRow(new Object[]{bilhete.getIdNota(),bilhete.getIdFuncionario(),bilhete.getData(),bilhete.getCpf(),bilhete.getTipoPagamento(),bilhete.getQtdIngressos()});
        }
        jtHistorico.setModel(modelo);
    }

