    public void refresh(){
        chairsSelected = null;
        chairsSelected = new ArrayList<>();
        String[]  idSelectedSalaArray  = cbFilmes.getSelectedItem().toString().split("Sala: ",-1);
        String[]  idSelectedSalaArray2 = idSelectedSalaArray[1].split(" "); //idSelectedSala2[0] pega a sala
        int idSelectedSala = Integer.parseInt(idSelectedSalaArray2[0]);
        try{
            ICadeiraDAO dao = new CadeiraDAO();
            List<Cadeira> lista = dao.listarTodos();
            
            for(Cadeira cadeira: lista){
                for(int i=0; i<btChairs.size(); i++){
                    if(btChairs.get(i).getActionCommand().equals(cadeira.getNumero())&&idSelectedSala==cadeira.getIdSala()&&cadeira.getEstado().equals("ocupado")){
                        btChairs.get(i).setEnabled(false);
                        
                    } else if(btChairs.get(i).getActionCommand().equals(cadeira.getNumero())&&idSelectedSala==cadeira.getIdSala()&&cadeira.getEstado().equals("livre")){
                        btChairs.get(i).setEnabled(true);
                    } 
                }
            }   
        } catch (Exception e){
            System.out.println(e.toString());
            JOptionPane.showMessageDialog(null, "Houve um erro ao carregar os dados...");
        }
        for(int i=0; i<btChairs.size();i++){
            if(btChairs.get(i).isSelected()==true){
                btChairs.get(i).setSelected(false);
            }
        }
        chairsSelected.clear();
        contador=0;
    }
