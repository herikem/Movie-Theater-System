    public void clickChair(int btNumber, java.awt.event.ActionEvent evt){
        if(btChairs.get(btNumber).isSelected()==true){
            contador++;
            chairsSelected.add(btChairs.get(btNumber).getActionCommand());
        }else{
            contador--;
            chairsSelected.remove(evt.getActionCommand());
        }
        if(limitador()==true){
            btChairs.get(btNumber).setSelected(false);
            contador--;
            chairsSelected.remove(btChairs.get(btNumber));
        }
        System.out.println(btChairs.get(btNumber).getActionCommand());
    }
    
    public boolean limitador(){
        boolean limit=false;
        if(contador>8){
            limit=true;
            JOptionPane.showMessageDialog(null, "Quantidade maxima de ingressos por compra é 8", "Erro", 1);
        }
        return limit;
    }
    
